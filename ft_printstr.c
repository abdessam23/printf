#include "printf.h"

int	ft_printstr(char *s)
{
	int	i;
	int	l;

	i = 0;
	l = 0;
	if (!s)
		return (0);
	while (s[l])
		l++;
	while (*s)
	{
		ft_printchar(*s);
		s++;
	}
	return (l);
}
